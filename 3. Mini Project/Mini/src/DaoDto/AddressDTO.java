package DaoDto;

public class AddressDTO {
	private String ad_id;
	private String ad_name;
	private String ad_department;
	private String ad_rank;
	private String ad_phone;
	private String ad_email;
	
	public AddressDTO() {}

	public String getAd_id() {
		return ad_id;
	}
	public void setAd_id(String ad_id) {
		this.ad_id = ad_id;
	}
	public String getAd_name() {
		return ad_name;
	}
	public void setAd_name(String ad_name) {
		this.ad_name = ad_name;
	}
	public String getAd_department() {
		return ad_department;
	}
	public void setAd_department(String ad_department) {
		this.ad_department = ad_department;
	}
	public String getAd_rank() {
		return ad_rank;
	}
	public void setAd_rank(String ad_rank) {
		this.ad_rank = ad_rank;
	}
	public String getAd_phone() {
		return ad_phone;
	}
	public void setAd_phone(String ad_phone) {
		this.ad_phone = ad_phone;
	}
	public String getAd_email() {
		return ad_email;
	}
	public void setAd_email(String ad_email) {
		this.ad_email = ad_email;
	}
	
}
